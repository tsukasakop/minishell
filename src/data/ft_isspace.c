#include <minishell.h>

/*
 * Function:ft_isspace
 * ----------------------------
 * Checks if a character is a whitespace.
 */
int	ft_isspace(int c)
{
	return (c == ' ' || c == '\t' || c == '\n' || \
			c == '\v' || c == '\f' || c == '\r');
}
